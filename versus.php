<p align="justify">Motion-Kenpo versus Sub-Level Four Kenpo&#8482;<br> A Sub-Level 
  Four&#8482; Self-Defense technique is uniquely different from Motion-Kenpo. 
  A technique in Motion-Kenpo is, among other things, primarily a study of the 
  effective use of &#8220;motion&#8221; in a combat scenario. Every hypothesis 
  or technique theme is based and predicated on &#8220;motion.&#8221; Additionally, 
  It does not explore hands on application of holds, hugs, locks, and seizures.<br>
  <br>
  A <strong>Sub-level Four&#8482;</strong> technique is a case study of many different complex 
  sciences presented in a practical application default technique modality, which 
  reaches well beyond its obvious immediate effectiveness. This &#8220;process&#8221; 
  subsequently addresses long term goals and applications.<br>
  <br>
  To this end through the entire first level of study, techniques are presented 
  as &#8220;hard curriculum&#8221; with absolutely no exploration or adjustment 
  of themes without instructor approval. This is not uncommon outside the bounds 
  of loose structured Motion-Kenpo. In general, minor &#8220;tailoring&#8221; 
  is only allowed to compensate for height or girth deficiencies. The proverbial 
  Kenpo hypothesis &#8220;what if&#8221; is not allowed or entertained, and there 
  are no Motion-Kenpo defined &#8220;re-arrangement concepts.&#8221;<br>
  <br>
  The base or default technique execution is conceptually inclusive of minor variables 
  without significant adjustment. Major variables are assigned different &#8220;Default 
  Techniques.&#8221; These things cannot be seen by the uneducated eye. When executed 
  properly, major benefit is attained because each individual technique functions 
  on multiple levels, and lays the base foundation for even more advanced application 
  of a theme allowing sophisticated &#8220;Destructive Modulation&#8221; at upper 
  levels.<br>
  <br>
  A technique teaches all the things listed below and additionally functions as 
  a &#8220;mini&#8221; <strong>Taiji Chi-Qung</strong> form that may be practiced singularly without 
  a partner to the same end without physical contact, when a partner is not available. 
  A student is encouraged to study and explore body mechanic enhancing &#8220;chi,&#8221; 
  and &#8220;chi&#8221; enhancing body movements, with as well as without a training 
  partner.<br>
  <br>
  Proper anatomical movement and internal energy co-exist hand in hand and one 
  cannot be attained without the presence of the other. Done properly, they spiral 
  upward together as long as you continue to practice, without age barriers. SL-4&#8482; 
  teaches immediate application of what the Chinese have traditionally waited 
  years to explain to a very few.<br>
  <br>
  It cannot be over emphasized, these things are not visible to the uneducated 
  eye, and cannot be understood anymore than you would movements of any discipline 
  not explained to you. <em>Sub-Level Four&#8482;</em> techniques are absolutely workable 
  and effective and there are no &#8220;throw away&#8221; techniques.<br>
  <br>
  What is immediately discernable is when, at higher levels, a Control manipulation 
  alternative might be to modulate destruction. This is what has prompted some 
  to suggest, &#8220;It&#8217;s just contact manipulation,&#8221; or &#8220;They 
  are just adding a manipulation to the technique.&#8221; Nothing could be further 
  from the truth.<br>
  <br>
  In fact, a student is taught to execute the more destructive &#8220;Destructive 
  Sequencing&#8221; first along with minor manipulations because full Control 
  Manipulations require a much greater degree of skill and a higher physical commitment 
  in training as well as practice. It is where the Ed Parker phrase &#8220;&#8230;to 
  feel is to believe&#8221; manifests itself.<br>
